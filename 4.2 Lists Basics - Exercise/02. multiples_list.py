
# Напишете програма, която получава две числа (множител и брой).
# Той трябва да създаде списък с дължина на дадения брой, който съдържа само цели числа,
# които са кратни на дадения фактор. Числата трябва да са само положителни и да са подредени във възходящ ред,
# започвайки от стойността на фактора.


factor = int(input())
count = int(input())
list_of_numbers = []
for multiplier in range(1, count + 1):
    list_of_numbers.append(factor * multiplier)
print(list_of_numbers)

